#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	s1_len;
	char	*str;
	int		i;

	i = 0;
	s1_len = ft_strlen(s1);
	str = malloc(sizeof(char) * s1_len + 1);
	if (!str)
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
